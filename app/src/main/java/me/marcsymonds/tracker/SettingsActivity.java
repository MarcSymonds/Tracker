    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        int id = item.getItemId();
        Log.d("OIS", String.format("onOptionsItemSelected %d", id));

        switch (id) {
            case android.R.id.home:
                setResult(0);
                finish();
                break;

            default:
                return false;
        }

        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean onIsMultiPane() {
        return isXLargeTablet(this);
    }

    /**
     * {@inheritDoc}
     */
    @Override
    @TargetApi(Build.VERSION_CODES.HONEYCOMB)
    public void onBuildHeaders(List<Header> target) {
        loadHeadersFromResource(R.xml.pref_headers, target);
    }

    /**
     * This method stops fragment injection in malicious applications.
     * Make sure to deny any unknown fragments here.
     */
    protected boolean isValidFragment(String fragmentName) {
        return PreferenceFragment.class.getName().equals(fragmentName)
                || HistoryUploadFragment.class.getName().equals(fragmentName);
        //|| GeneralPreferenceFragment.class.getName().equals(fragmentName)
        //|| DataSyncPreferenceFragment.class.getName().equals(fragmentName)
        //|| NotificationPreferenceFragment.class.getName().equals(fragmentName);
    }

    /**
     * This fragment shows general preferences only. It is used when the
     * activity is showing a two-pane settings UI.
     */

    @TargetApi(Build.VERSION_CODES.HONEYCOMB)
    public static class HistoryUploadFragment extends PreferenceFragment {
        private static final PreferenceBinder mPreferenceBinder = new PreferenceBinder();

        @Override
        public void onCreate(Bundle savedInstanceState) {
            super.onCreate(savedInstanceState);
            addPreferencesFromResource(R.xml.pref_history_upload);

            mPreferenceBinder.bindControls(getPreferenceScreen());

            setHasOptionsMenu(true);
        }

        @Override
        public boolean onOptionsItemSelected(MenuItem item) {
            int id = item.getItemId();
            if (id == android.R.id.home) {
                Log.d("HUF", "Restarting SettingsActivity");
                startActivity(new Intent(getActivity(), SettingsActivity.class));
                return true;
            }
            return super.onOptionsItemSelected(item);
        }
    }

